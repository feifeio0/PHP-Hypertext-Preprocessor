<?php 

//1.数组没有键,从0到n自动分配
$a = array('meinv','shuaige','laoyeye');//加入只给一个数据,默认是作为值存在
print_r($a);
//2.有的键分配,有的没有(没有分配键的会根据之前的最大值进行递增)
$b = array(3=>'巧克力','奶茶','好'=>'猪头肉','33'=>'鱼头','柴鸡');
print_r($b);
//3.如果健分配重复了
$c = array('2'=>'nihao',3=>'meinv',2=>'shuaige','a','b','c');
print_r($c);
//4.
//(1)数组的键只能有2种类型,整型,字符串
//(2)假如键为浮点数向下取整
//(3)字符串型整数会自动转为整型
//(4)假如键为null会自动转为空字符串
$arr = array(2=>'布',2.5=>'尔','2.5'=>'教','2'=>'育',null=>'好');
print_r($arr);
?>